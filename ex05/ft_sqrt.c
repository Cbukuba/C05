int	ft_sqrt(int nb)
{
	int	i;
	int	p;

	i = 0;
	while (i <= nb)
	{
		p = i * i;
		if (p == nb)
			if (i <= 46340)
				return (i);
		i ++;
	}
	return (0);
}

